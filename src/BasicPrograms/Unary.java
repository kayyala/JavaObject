package BasicPrograms;

public class Unary {

    public static void main(String[] args){
        int x=20, y=40;
        int result = +x;
        System.out.println(result);
        result = -x;
        System.out.println(result);
        result = ++x;
        System.out.println(result);
        result = --x;
        System.out.println(result);
        //Logical NOT
        boolean value = false;
        System.out.println(value);
        System.out.println(!value);
    }
}
