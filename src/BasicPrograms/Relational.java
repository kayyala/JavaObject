package BasicPrograms;


public class Relational {
    public static void main(String[] args){
        int x=20;
        int y=30;
        boolean result = x==y;
        System.out.println("x==y ? " + result);
        result= x!=y;
        System.out.println("x!=y ? " + result);
        result = x>y;
        System.out.println("x>y ?" + result);
        result = x>=y;
        System.out.println("x<y ?" + result);
        result = x<y;
        System.out.println("x<=y ?" + result);
        result = x<=y;
        System.out.println("x<=y ?" + result);

    }
}
